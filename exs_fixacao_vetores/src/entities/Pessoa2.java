package entities;

public class Pessoa2 {

	private double altura;
	private char genero;
	
	
	public Pessoa2(double altura, char genero) {
		this.altura = altura;
		this.genero = genero;
	}
	
	public double getAltura() {
		return altura;
	}
	
	public char getGenero() {
		return genero;
	}
}
